
public class PDT_int {

	public static void main(String[] args) {
		// 정수형: 2진수, 8진수, 10진수, 16진수
		
		//1. 2진수 ( 0/1) : 0b값
		System.out.println(0b01);  //1
		System.out.println(0b10);  //2
		System.out.println(0b11);  //3
		System.out.println(0b100); //4
		System.out.println("##########################");
	   //2. 8진수	 ( 0 ~ 7 ) : 0값
       System.out.println(01);
       System.out.println(02);
       System.out.println(03);
       System.out.println(04);
       System.out.println(05);
       System.out.println(06);
       System.out.println(07);
       System.out.println("##########################");
       //3. 16진수 ( 0~15 ==> 0~9,A,B,C,D,E,F )==> 0x값 
       // 주소값을 표현할 때 주로 사용됨.
       System.out.println(0x0);
       System.out.println(0x1);
       System.out.println(0x2);
       System.out.println(0x3);
       System.out.println(0x4);
       System.out.println(0x5);
       System.out.println(0x6);
       System.out.println(0x7);
       System.out.println(0x8);
       System.out.println(0x9);
       System.out.println(0xA);
       System.out.println(0xB);
       System.out.println(0xC);
       System.out.println(0xD);
       System.out.println(0xE);
       System.out.println(0xF);
	}

}
